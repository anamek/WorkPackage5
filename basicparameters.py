import math

#-----------------------------------------------------------------------------------------------------------------------
def moment_of_inertia (t_1, d):
    I = (math.pi * t_1 * d ** 3) / (8)
    return I

def area (t_1, r):
    A = (math.pi * r ** 2) - (math.pi * (r - t_1) ** 2)
    return A
